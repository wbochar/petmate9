
; Petmate9 Horizontal Smooth Scroller (C64) — Double Buffered
; Source: c64-4xwidth (160x25), scroll 120 cols
; Buffer A = $0400, Buffer B = $0C00 — swap via $D018 in IRQ
!include "macros.asm"
!let music_load = $5000
!let music_init = $5F80
!let music_play = $5012

!let irq_top_line    = 1
!let irq_bottom_line = 251
!let debug_build     = FALSE
!let VISIBLE_COLS    = 40
!let VISIBLE_ROWS    = 25
!let MAX_SCROLL      = 120
!let SRC_ROW_WIDTH   = 160
!let SCROLL_SPEED    = 1
!let D018_A          = $15
!let D018_B          = $35

!let zp_src          = $20
!let zp_dst          = $22
!let zp_vis_row      = $24
!let zp_col_lo       = $25   ; scrollCol low byte for source offset
!let zp_col_hi       = $26   ; scrollCol high byte (always 0 for col<256)
!let zp_cmt_src      = $28   ; dedicated commit-source pointer (IRQ-safe)


+basic_start(entry)

;--------------------------------------------------------------
; Entry
;--------------------------------------------------------------
entry: {

    sei
    lda #$35
    sta $01
    lda #0
    jsr music_init
    +setup_irq(irq_top, irq_top_line)
    cli

    ; Border / background
    lda #14
    sta $d020
    lda #6
    sta $d021

    ; Init scroll state
    lda #7
    sta scrollFine
    lda #0
    sta scrollCol
    sta displayBuf
    sta workBufOffset
    sta coarsePhase
    sta scrollDir           ; 0 = forward/right, 1 = reverse/left (pingpong mode)
    sta paused
    sta muteFlag
    sta keySpacePrev
    sta keyMPrev
    sta keyPlusPrev
    sta keyMinusPrev
    lda #$ff
    sta $dc00
    sta $dc02               ; CIA1 port A output (keyboard row select)
    lda #$00
    sta $dc03               ; CIA1 port B input  (keyboard columns)
    lda #SCROLL_SPEED
    sta scrollSpeed
    sta delayCounter

    ; Pre-fill BOTH buffers so the first swap is already ready
    ; Buffer A (workBufOffset = $00 → dest hi $04 → $0400)
    jsr copy_window_screen
    jsr copy_window_color
    ; Buffer B (workBufOffset = $08 → dest hi $04+$08=$0C → $0C00)
    lda #$08
    sta workBufOffset
    jsr copy_window_screen
    lda #$00
    sta workBufOffset

    ; Set VIC: 25 rows, YSCROLL=3 (normal), 38 cols, XSCROLL=7
    lda #$1b            ; 25-row mode, normal
    sta $d011
    lda #$c7            ; 38-col mode ($C0) + XSCROLL=7
    sta nextD016
    sta $d016
    lda #D018_A
    sta nextD018
    sta $d018

    ; ── Main loop: double-buffered prepare-ahead ──

main_loop:
    lda vsyncFlag
    beq main_loop
    lda #0
    sta vsyncFlag
    lda paused
    beq not_paused
    jmp post_frame_work
not_paused:

    dec delayCounter
    beq delay_elapsed
    jmp post_frame_work
delay_elapsed:
    lda scrollSpeed
    sta delayCounter

    ; ── Prepare next scroll step ──

    ; Pingpong fine+coarse stepping with smooth turnarounds
    lda scrollDir
    bne pp_reverse
    jmp pp_fwd_fine
pp_reverse:
    inc scrollFine
    lda scrollFine
    cmp #8
    bcs pp_rev_crossed
    jmp prep_fine
pp_rev_crossed:
    ; Reverse coarse boundary crossed
    lda scrollCol
    bne pp_rev_do_coarse
    ; At left edge: turn around and start moving forward
    lda #7
    sta scrollFine
    lda #0
    sta scrollDir
    jmp prep_fine
pp_rev_do_coarse:
    lda #0
    sta scrollFine
    dec scrollCol
    jmp do_coarse

pp_fwd_fine:
    dec scrollFine
    bmi pp_fwd_crossed
    jmp prep_fine
pp_fwd_crossed:
    ; Forward coarse boundary crossed
    lda scrollCol
    cmp #MAX_SCROLL
    bcc pp_fwd_do_coarse
    ; At right edge: turn around and start moving reverse
    lda #0
    sta scrollFine
    lda #1
    sta scrollDir
    jmp prep_fine
pp_fwd_do_coarse:
    lda #7
    sta scrollFine
    inc scrollCol


do_coarse:
    ; Determine work-buffer offset ($00 = $0400, $08 = $0C00)
    lda displayBuf
    bne work_is_a
    lda #$08
    jmp set_work
work_is_a:
    lda #$00
set_work:
    sta workBufOffset

    ; Flip displayBuf and queue $D018 swap BEFORE the long copy and
    ; BEFORE arming coarsePhase. That way, when the lower-border IRQ
    ; fires mid-copy and commits bottom (phase 1→2), the very next
    ; upper-border IRQ already has nextD018 ready to apply.
    lda displayBuf
    eor #1
    sta displayBuf
    beq swap_a
    lda #D018_B
    jmp queue_swap
swap_a:
    lda #D018_A
queue_swap:
    sta nextD018

    ; Arm the commit BEFORE the char copy so the lower-border IRQ
    ; never misses the signal no matter how long the copy takes.
    ; The commit uses zp_cmt_src (not zp_src), so it's safe to preempt
    ; copy_row mid-byte.
    lda #1
    sta coarsePhase

    ; Big 25×40 char copy — may be preempted by the lower-border IRQ.
    ; Color RAM is NOT touched here; the IRQ chain commits $D800
    ; across frame N-1 lower border + frame N upper border.
    jsr copy_window_screen

prep_fine:
    lda #$c0            ; 38-col mode
    ora scrollFine
    sta nextD016
post_frame_work:
    jsr handle_input

    lda muteFlag
    beq music_unmuted
    lda #0
    sta $d404
    sta $d40b
    sta $d412
    sta $d417
    sta $d418
    jmp music_main_done
music_unmuted:
    jsr music_play
music_main_done:

    jmp main_loop
}

;--------------------------------------------------------------
; handle_input — edge-triggered controls:
;   SPACE toggles pause
;   M toggles mute
;   + speeds up  (min delay=1)
;   - slows down (max delay=255)
;--------------------------------------------------------------
handle_input: {
    ; SPACE key: row 7, column 4
    lda #$7f
    sta $dc00
    lda $dc01
    and #$10
    bne space_up
space_down:
    lda keySpacePrev
    bne space_done
    lda #1
    sta keySpacePrev
    lda paused
    eor #1
    sta paused
space_done:
    jmp check_m
space_up:
    lda #0
    sta keySpacePrev

check_m:
    ; M key: row 4, column 4
    lda #$ef
    sta $dc00
    lda $dc01
    and #$10
    bne m_up
m_down:
    lda keyMPrev
    bne m_done
    lda #1
    sta keyMPrev
    lda muteFlag
    eor #1
    sta muteFlag
m_done:
    jmp check_speed
m_up:
    lda #0
    sta keyMPrev

check_speed:
    ; + / - keys: row 5, columns 0 and 3
    lda #$df
    sta $dc00
    lda $dc01
    sta keyRowState

    lda keyRowState
    and #$01
    bne plus_up
plus_down:
    lda keyPlusPrev
    bne plus_done
    lda #1
    sta keyPlusPrev
    lda scrollSpeed
    cmp #1
    beq plus_done
    dec scrollSpeed
plus_done:
    jmp check_minus
plus_up:
    lda #0
    sta keyPlusPrev

check_minus:
    lda keyRowState
    and #$08
    bne minus_up
minus_down:
    lda keyMinusPrev
    bne minus_done
    lda #1
    sta keyMinusPrev
    lda scrollSpeed
    cmp #255
    beq minus_done
    inc scrollSpeed
minus_done:
    jmp input_done
minus_up:
    lda #0
    sta keyMinusPrev

input_done:
    lda #$ff
    sta $dc00
    rts
}

;--------------------------------------------------------------
; copy_window_screen – extract 40-col strip from 160-col source
;   Reads from src_scr_row_base[row] + scrollCol
;   Writes to screen_dest[row] + workBufOffset
;--------------------------------------------------------------
copy_window_screen: {
    lda scrollCol
    sta zp_col_lo
    lda #0
    sta zp_col_hi       ; scrollCol < 256, but carry may bump hi
    sta zp_vis_row

row_loop:
    ldx zp_vis_row
    ; Source = src_scr_row_base + scrollCol
    lda src_scr_row_lo,x
    clc
    adc zp_col_lo
    sta zp_src
    lda src_scr_row_hi,x
    adc zp_col_hi
    sta zp_src+1

    ; Dest = screen_dest + workBufOffset
    lda screen_dest_lo,x
    sta zp_dst
    lda screen_dest_hi,x
    clc
    adc workBufOffset
    sta zp_dst+1

    jsr copy_row

    inc zp_vis_row
    lda zp_vis_row
    cmp #VISIBLE_ROWS
    bne row_loop
    rts
}

;--------------------------------------------------------------
; copy_window_color – initial fill of LIVE $D800 during setup only.
; During scroll, $D800 is updated by irq_top/irq_bottom.
;--------------------------------------------------------------
copy_window_color: {
    lda scrollCol
    sta zp_col_lo
    lda #0
    sta zp_col_hi
    sta zp_vis_row

row_loop:
    ldx zp_vis_row
    lda src_col_row_lo,x
    clc
    adc zp_col_lo
    sta zp_src
    lda src_col_row_hi,x
    adc zp_col_hi
    sta zp_src+1

    lda color_dest_lo,x
    sta zp_dst
    lda color_dest_hi,x
    sta zp_dst+1

    jsr copy_row

    inc zp_vis_row
    lda zp_vis_row
    cmp #VISIBLE_ROWS
    bne row_loop
    rts
}

