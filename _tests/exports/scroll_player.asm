
; Petmate9 Vertical Smooth Scroller (C64) — Double Buffered
; Source: c64-4xheight (40x100), scroll 75 rows
; Buffer A = $0400, Buffer B = $0C00 — swap via $D018 in IRQ
!include "macros.asm"



!let irq_top_line  = 1
!let debug_build   = FALSE
!let TOTAL_ROWS    = 100
!let VISIBLE_ROWS  = 25
!let MAX_SCROLL    = 75
!let ROW_WIDTH     = 40
!let SCROLL_SPEED  = 1
!let D018_A        = $15
!let D018_B        = $35

!let zp_src        = $20
!let zp_dst        = $22
!let zp_src_row    = $24
!let zp_vis_row    = $25

+basic_start(entry)

;--------------------------------------------------------------
; Entry
;--------------------------------------------------------------
entry: {



    sei
    lda #$35
    sta $01
    +setup_irq(irq_top, irq_top_line)
    cli

    ; Border / background
    lda #14
    sta $d020
    lda #6
    sta $d021

    ; Init scroll state
    lda #7
    sta scrollFine
    lda #0
    sta scrollRow
    sta displayBuf          ; 0 = showing buffer A
    sta workBufOffset       ; 0 = writing to $0400 (initial fill)
    lda #SCROLL_SPEED
    sta delayCounter

    ; Pre-fill BOTH buffers so the first swap is already ready
    ; Buffer A (workBufOffset=0)
    jsr copy_window_screen
    jsr copy_window_color
    ; Buffer B (workBufOffset=8)
    lda #$08
    sta workBufOffset
    jsr copy_window_screen
    lda #$00
    sta workBufOffset

    ; Set initial VIC registers
    lda #$17                ; 24-row mode, YSCROLL=7
    sta nextD011
    sta $d011
    lda #D018_A
    sta nextD018
    sta $d018

    ; ── Main loop: double-buffered prepare-ahead ──
    ; Fine-scroll: just queue nextD011.
    ; Coarse-scroll: write 25 rows to the OFF-SCREEN buffer
    ; (no tearing), update color RAM, then queue buffer swap.
    ; IRQ applies $D011 + $D018 atomically at a fixed raster line.

main_loop:
    lda vsyncFlag
    beq main_loop
    lda #0
    sta vsyncFlag

    dec delayCounter
    bne main_loop
    lda #SCROLL_SPEED
    sta delayCounter

    ; ── Prepare next scroll step ──
    dec scrollFine
    bpl prep_fine

    ; Coarse scroll needed
    lda #7
    sta scrollFine

    inc scrollRow
    lda scrollRow
    cmp #MAX_SCROLL
    bcc do_coarse
    ; Wrap
    lda #0
    sta scrollRow

do_coarse:
    ; Determine work-buffer offset ($00 = $0400, $08 = $0C00)
    lda displayBuf
    bne work_is_a
    lda #$08                ; displaying A → work is B
    jmp set_work
work_is_a:
    lda #$00                ; displaying B → work is A
set_work:
    sta workBufOffset

    ; Copy 25 rows from source to work buffer (invisible — no tearing)
    jsr copy_window_screen
    ; Copy 25 rows from source to $D800 (live color RAM)
    jsr copy_window_color

    ; Queue buffer swap
    lda displayBuf
    eor #1
    sta displayBuf
    beq swap_a
    lda #D018_B
    jmp queue_swap
swap_a:
    lda #D018_A
queue_swap:
    sta nextD018

prep_fine:
    lda #$10
    ora scrollFine
    sta nextD011
    jmp main_loop
}

;--------------------------------------------------------------
; copy_window_screen – 25 rows from source to work buffer
;   workBufOffset: $00 writes to $04xx, $08 writes to $0Cxx
;--------------------------------------------------------------
copy_window_screen: {
    lda scrollRow
    sta zp_src_row
    lda #0
    sta zp_vis_row
row_loop:
    ldx zp_src_row
    lda scr_row_lo,x
    sta zp_src
    lda scr_row_hi,x
    sta zp_src+1
    ldx zp_vis_row
    lda screen_dest_lo,x
    sta zp_dst
    lda screen_dest_hi,x
    clc
    adc workBufOffset       ; +$00 for A, +$08 for B
    sta zp_dst+1
    jsr copy_row
    inc zp_src_row
    inc zp_vis_row
    lda zp_vis_row
    cmp #VISIBLE_ROWS
    bne row_loop
    rts
}

;--------------------------------------------------------------
; copy_window_color – 25 rows from source to $D800
;--------------------------------------------------------------
copy_window_color: {
    lda scrollRow
    sta zp_src_row
    lda #0
    sta zp_vis_row
row_loop:
    ldx zp_src_row
    lda col_row_lo,x
    sta zp_src
    lda col_row_hi,x
    sta zp_src+1
    ldx zp_vis_row
    lda color_dest_lo,x
    sta zp_dst
    lda color_dest_hi,x
    sta zp_dst+1
    jsr copy_row
    inc zp_src_row
    inc zp_vis_row
    lda zp_vis_row
    cmp #VISIBLE_ROWS
    bne row_loop
    rts
}

;--------------------------------------------------------------
; copy_row – copy ROW_WIDTH bytes from (zp_src) to (zp_dst)
;--------------------------------------------------------------
copy_row: {
    ldy #ROW_WIDTH-1
loop:
    lda (zp_src),y
    sta (zp_dst),y
    dey
    bpl loop
    rts
}

;--------------------------------------------------------------
; Raster IRQ — applies $D011 and $D018 at fixed raster position
;--------------------------------------------------------------
irq_top: {
    +irq_start(end)

    lda nextD011
    sta $d011
    lda nextD018
    sta $d018

    lda #1
    sta vsyncFlag

!if (debug_build) {
    inc $d020
}

!if (debug_build) {
    dec $d020
}
    +irq_end(irq_top, irq_top_line)
end:
}

